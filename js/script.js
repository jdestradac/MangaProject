const generatePopularSection = () => {
    const container = document.querySelector('#Popular');
    container.innerHTML = '';

    const sliderContainer = document.createElement('div');
    sliderContainer.className = 'slider';

    slider.slice(3, 9).map((item) => {
        const sliderItem = document.createElement('div');
        sliderItem.className = 'slider-item';

        const overlayDiv = document.createElement('div');
        overlayDiv.className = 'overlay';

        sliderItem.style.backgroundImage = `url(${item.banner})`;

        const sliderTitle = document.createElement('h2');
        sliderTitle.textContent = item.title;

        overlayDiv.appendChild(sliderTitle);
        sliderItem.appendChild(overlayDiv);
        sliderContainer.appendChild(sliderItem);

        sliderItem.onclick = function () {
            openDetailPage(item.id); 
        };

        return sliderItem; 
    });

    container.appendChild(sliderContainer);
};


const openDetailPage = (itemId) => {
    window.location.href = `./views/detail.html?item=${itemId}`;
}

generatePopularSection();


